using AutoMapper;
using BitirmeProjesi.Common.DTOs.OrderRefundRequestItem;
using BitirmeProjesi.Common.Extensions;
using BitirmeProjesi.Model.Entities;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace BitirmeProjesi.API.Infrastructor.Mapper
{
    public class OrderRefundRequestItemMapperProfile : Profile
    {
        public OrderRefundRequestItemMapperProfile()
        {
            CreateMap<OrderRefundRequestItem, OrderRefundRequestItemRequest>()
                 .ReverseMap()
                 .IgnoreAllNonExisting()
                 .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<OrderRefundRequestItem, OrderRefundRequestItemResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

        }
    }
}
